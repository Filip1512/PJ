public class Calculator {

        public int calculateSum(int x, int y){

            return x + y;
        }

        public int calculateSubstract(int x, int y){

            return x - y;
        }

        public int calculateMultiply(int x, int y){

            return x * y;
        }

        public int calculateDiv(int x, int y){

            return x / y;
        }
}
